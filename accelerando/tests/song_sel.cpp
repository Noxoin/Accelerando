void close()
{
	//Deallocate surfaces
	for( int i = 0; i < KEY_PRESS_SURFACE_TOTAL; ++i )
	{
		SDL_DestroyTexture( gKeyPressSurfaces[ i ] );
		gKeyPressSurfaces[ i ] = NULL;
	}
    //Free loaded images
	gDotTexture.free();
    
	//Destroy window
	SDL_DestroyRenderer( gRenderer );
	SDL_DestroyWindow( gWindow );
	gWindow = NULL;
	gRenderer = NULL;

	//Quit SDL subsystems
	IMG_Quit();
	SDL_Quit();
}

SDL_Texture* loadTexture( std::string path )
{
	//The final texture
	SDL_Texture* newTexture = NULL;

	//Load image at specified path
	SDL_Surface* loadedSurface = IMG_Load( path.c_str() );
	if( loadedSurface == NULL )
	{
		printf( "Unable to load image %s! SDL_image Error: %s\n", path.c_str(), IMG_GetError() );
	}
	else
	{
		//Create texture from surface pixels
        newTexture = SDL_CreateTextureFromSurface( gRenderer, loadedSurface );
		if( newTexture == NULL )
		{
			printf( "Unable to create texture from %s! SDL Error: %s\n", path.c_str(), SDL_GetError() );
		}

		//Get rid of old loaded surface
		SDL_FreeSurface( loadedSurface );
	}

	return newTexture;
}

int main( int argc, char* args[] )
{
	//Start up SDL and create window
	if( !init() )
	{
		printf( "Failed to initialize!\n" );
	}
	else
	{
		//Load media
		if( !loadMedia() )
		{
			printf( "Failed to load media!\n" );
		}
		else
		{	
			//Main loop flag
			bool quit = false;
			//Go to song selection screen flag
            bool go_to_song_sel = false;

			//Event handler
			SDL_Event e;

			//Set default current surface
			gTexture = gKeyPressSurfaces[ KEY_PRESS_SURFACE_DEFAULT ];
            
            //The dot that will be pointing to current song
			Dot dot;

			//While application is running
			while( !quit ){
				//Handle events on queue
				while( SDL_PollEvent( &e ) != 0 ){
					//User requests quit
					if( e.type == SDL_QUIT ){
						quit = true;
					}
					//Handle input for the dot
                    if (go_to_song_sel){dot.handleEvent( e );}
					//User presses a key
					if( (e.type == SDL_KEYDOWN) && (e.key.keysym.sym == SDLK_RETURN)){
                        gTexture = gKeyPressSurfaces[ KEY_PRESS_SURFACE_ENTER ];
                        go_to_song_sel = true;
                    }
                }
                
				//Move the dot
				if (go_to_song_sel){dot.move();}

				//Clear screen
                SDL_SetRenderDrawColor( gRenderer, 0xFF, 0xFF, 0xFF, 0xFF );
				SDL_RenderClear( gRenderer );

                //Render texture to screen
				SDL_RenderCopy( gRenderer, gTexture, NULL, NULL );
                //Render objects
                // !!DOT HAS TO BE RENDERED AFTER TEXTURE!!
				if (go_to_song_sel){dot.render();}
                
				//Update screen
				SDL_RenderPresent( gRenderer );
			}
		}
	}

	//Free resources and close SDL
	close();

	return 0;
}
